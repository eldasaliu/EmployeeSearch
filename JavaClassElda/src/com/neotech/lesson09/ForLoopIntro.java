package com.neotech.lesson09;

public class ForLoopIntro {

	public static void main(String[] args) {
		//I want to print numbers 1 from to 5
		int num=1;
		while(num<=5) {
			System.out.println(num);
			num++;
			
			// when we know the number of interetions ahead of time
			//t better when we know in advance how many times we want to repeat the block of cods
			//for(initialization
		}
		System.out.println("==========");
		//3 parts of a for loop
		//initializatin; condition; increment
		for(int a=1; a<=5; a++) {
			System.out.println(a);
		}
	}

}
